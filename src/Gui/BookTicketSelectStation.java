    private void BookticketSelectStationSubmitActionPerformed(java.awt.event.ActionEvent evt) {                                                              
        // TODO add your handling code here:
        String boardingStationCode = txtBoardingSta.getText().trim();
        String destinationStationCode = txtDestnationSta.getText().trim();
        if(txtBoardingSta.getText().equals("")&&txtDestnationSta.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Blank");
        }
        boolean validBoardingStation = false;
        boolean validDestinationStation = false;
        for (Station station : train.stations) {
            if (station.stacode.equals(boardingStationCode)) {
                validBoardingStation = true;
            }
            if (station.stacode.equals(destinationStationCode)) {
                validDestinationStation = true;
            }
        }
        if(validBoardingStation&&validDestinationStation){
            Trainreservation trainreserv=Trainreservation.getInstance();
            user.BookTicket(train, boardingStationCode, destinationStationCode);
            trainreserv.addtickets();
            JOptionPane.showMessageDialog(this, "your Ticket is successfuly Generated you can view in show ticket.");
            userInterface.setTempUsername(user.name);
            userInterface.setVisible(true);
            this.dispose();
        }
        if (!validBoardingStation ||!validDestinationStation) {
            JOptionPane.showMessageDialog(this, "Invalid station code(s) entered.");
            return;
        }
    }                                                             

