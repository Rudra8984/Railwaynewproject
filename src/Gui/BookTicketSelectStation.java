    public void fetchData(Trainreservation.User user,Traininfo train){
       this.user=user;
       this.train=train;
       this.stations=train.stations;
       populateTable(stations);
    }
    public void populateTable(List<Station> stations) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear the table before populating

        for (Station station : stations) {
            Object[] rowData = {
                station.stacode,
                station.station,
                station.arravingtime,
                station.deppaturetime
            };
            model.addRow(rowData); // Add station details to the table
        }
    }
