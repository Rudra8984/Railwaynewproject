    private void BookTicketSubmitActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        // TODO add your handling code here:
        String trainnostr = txtTrainNo.getText();
        int Trainno = 0; 
        try {
            Trainno = Integer.parseInt(trainnostr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid TrainNo format!");
            return; 
        }
        boolean trainFound = false;
        for (Traininfo train : trains) {
            if (train.gettrainno() == Trainno) {
                BookTicketSelectStation bookticket = new BookTicketSelectStation();
                bookticket.fetchData(user, train);
                bookticket.setVisible(true);
                this.dispose();
                trainFound = true;
                break;
            }
        }
        if (!trainFound) {
            JOptionPane.showMessageDialog(this, "Train number not found!");
        }
    }                                                

