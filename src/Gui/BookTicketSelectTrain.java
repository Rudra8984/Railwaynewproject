    public void fetchTrain(Trainreservation.User user){
        Trainreservation trainreserv=Trainreservation.getInstance();
        this.trains=trainreserv.Trains;
        this.user=user;
        populateTable(trains);
    }
    private void populateTable(List<Traininfo> trainList) {
        DefaultTableModel model = (DefaultTableModel) TrainListTable.getModel();
        model.setRowCount(0); 
        for (Traininfo train : trainList) {
            Object[] rowData = {
                train.trainnumber,
                train.trainname,
                train.sourseStation,
                train.destitionStation
            };
            model.addRow(rowData); 
        }
    }
