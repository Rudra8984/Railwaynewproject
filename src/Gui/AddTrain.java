    private void RmtrainActionActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
            String trainNoStr = rmtrain.getText();

        if (trainNoStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the Train No to remove.");
            return;
        }

        try {
            int trainNumberToRemove = Integer.parseInt(trainNoStr);
            Traininfo trainToRemove = null;
            for (Traininfo train : Trainreservation.getInstance().Trains) {
                if (train.trainnumber == trainNumberToRemove) {
                    trainToRemove = train;
                    break;
                }
            }
            if (trainToRemove != null) {
                Trainreservation.getInstance().Trains.remove(trainToRemove);
                updateTable();
                this.dispose();
                AddTrain addtrain=new AddTrain();
                addtrain.fetchData();
                addtrain.setVisible(true);
                JOptionPane.showMessageDialog(this, "Train removed successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Train with Train No. " + trainNumberToRemove + " not found.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Train No.");
        }
    }                                             
