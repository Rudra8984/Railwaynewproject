    public void fetchData(){
        Trainreservation trainreserv=Trainreservation.getInstance();
        List<Traininfo> trainList = trainreserv.Trains; // Assuming Trains is a list of Train objects
        populate(trainList);
    }
    public void populate(List<Traininfo> trainList) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear the table before populating

        for (Traininfo train : trainList) {
            Object[] rowData = {
                train.trainnumber,
                train.trainname,
                train.sourseStation,
                train.destitionStation
            };
            model.addRow(rowData); 
        }
    }
    public void addTrainManually(Traininfo newTrain) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] rowData = {
            newTrain.trainnumber,
            newTrain.trainname,
            newTrain.sourseStation,
            newTrain.destitionStation
        };
        model.addRow(rowData); 
    }
