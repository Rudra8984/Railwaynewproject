    private void AddtrainSubmitActionPerformed(java.awt.event.ActionEvent evt) {                                               
        // TODO add your handling code here:
        if(txtTrainNoAddTrain.getText().equals("")&&txtTrainNameAddTrain.getText().equals("")&&txtStartPointAddTrain.getText().equals("")&&txtEndPointAddTrain.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Blank Not Allowed");
        }else{
            String trainNoStr = txtTrainNoAddTrain.getText();
            int trainNumber = 0; // Default value if parsing fails
            try {
                trainNumber = Integer.parseInt(trainNoStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Train number should be a valid integer!");
                return; // Exit the method if parsing fails
            }
            temptrain=new Traininfo(trainNumber,txtTrainNameAddTrain.getText(),txtStartPointAddTrain.getText(),txtEndPointAddTrain.getText());
            Trainreservation trainreserv=Trainreservation.getInstance();
            trainreserv.addtrain(temptrain);
            addTrainManually(temptrain);
            AddStation addstation=new AddStation();
            addstation.fetchData(temptrain);
            addstation.setVisible(true);
            this.dispose();
        }
    }                                              
